using ll = long long;

constexpr ll MOD = 1e9+7;
constexpr ll MAXN = 100000;

ll inv(long long i){
    return i <= 1 ? i : MOD - (MOD / i) * inv(MOD % i) % MOD;
}

void solve(){
    int n, m, p;
    std::cin >> n >> m >> p;

    std::cout << (m <= n) + std::max(0, (n - m) / p) << '\n';
}

